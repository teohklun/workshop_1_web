CREATE DATABASE test;

use test;

CREATE TABLE Address (
    AddressID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    City      VARCHAR (255) NULL,
    State     VARCHAR (255) NULL,
    Postcode  VARCHAR (255) NULL,
    Street    VARCHAR (255) NULL
);

CREATE TABLE Profile (
    ProfileID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    ContactNo INT NULL,
    Name      VARCHAR (255) NULL,
    IC        VARCHAR (255) NULL,
    Email     VARCHAR (255) NULL,
    ProfilePath     VARCHAR (255) NULL,
    AddressID INT  			UNSIGNED NULL,
	CONSTRAINT FK_Address_Profile FOREIGN KEY (AddressID)
    REFERENCES Address(AddressID)
);

 CREATE TABLE Users (
    UserID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    Username  VARCHAR (255) NULL,
    Password  VARCHAR (255) NULL,
    Type      INT           DEFAULT 0 NULL,
    ProfileID INT  			UNSIGNED NULL,
	FOREIGN KEY (ProfileID) REFERENCES Profile(ProfileID)
);

CREATE TABLE Patient (
    PatientID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    Debt      DECIMAL (8) NULL,
    ProfileID INT  			UNSIGNED NULL,
    UpdatedAt DATETIME    DEFAULT current_timestamp,
    CreatedAt DATETIME    DEFAULT current_timestamp,
    UpdatedBy INT         NULL,
    CreatedBy INT         NULL,
	FOREIGN KEY (ProfileID) REFERENCES Profile(ProfileID)
);

CREATE TABLE Doctor (
    DoctorID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    ProfileID INT  			UNSIGNED NULL,
    Professional  VARCHAR (255) NULL,
    UpdatedAT    DATETIME       DEFAULT current_timestamp,
	FOREIGN KEY (ProfileID) REFERENCES Profile(ProfileID)
);

CREATE TABLE Schedule (
    ScheduleID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    Date       DATE     NULL,
    StartTime       TIME (0) NULL,
    EndTime       TIME (0) NULL,
    DoctorID INT  			UNSIGNED NULL,
    SlotAvailable INT UNSIGNED NOT NULL,
    CreatedAt  DATETIME DEFAULT current_timestamp NOT NULL,
    UpdatedAt  DATETIME DEFAULT current_timestamp NOT NULL,
    CreatedBy  INT      NULL,
    UpdatedBy  INT      NULL,
	FOREIGN KEY (DoctorID) REFERENCES Doctor(DoctorID)
);

CREATE TABLE Appointment (
    AppointmentID INT   UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    Time          TIME (0)       NULL,
    EndTime          Time(0)       NULL,
    Remark  	  text NULL,
    Completed     INT            DEFAULT 0 NULL,
    TreatmentDesc  VARCHAR (255) NULL,
    Price         DECIMAL (8, 2) DEFAULT 0 NULL,
    PatientID INT  			UNSIGNED NULL,
    ScheduleID INT  			UNSIGNED NULL,
    UpdatedAt     DATETIME       DEFAULT current_timestamp NOT NULL,
    CreatedAt     DATETIME       DEFAULT current_timestamp NOT NULL,
    CreatedBy     INT            NULL,
    UpdatedBy     INT            NULL,
	FOREIGN KEY (ScheduleID) REFERENCES Schedule(ScheduleID),
	FOREIGN KEY (PatientID) REFERENCES Patient(PatientID)
);

